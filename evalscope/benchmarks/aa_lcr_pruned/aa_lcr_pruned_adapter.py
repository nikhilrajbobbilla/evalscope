"""AA-LCR Pruned adapter — runs the standard AA-LCR scoring pipeline against
a curated subset of indices produced by an external pruner.

Usage:
    evalscope eval --model <model> --datasets aa_lcr_pruned \\
        --extra-args '{"selected_indices_path": "/path/to/manifests/aa_lcr_pruned.json"}'

Mirrors the live_code_bench_pruned adapter — only the upstream parent class
and dataset_id change.
"""
import json
from pathlib import Path
from typing import Any, Dict

from evalscope.api.benchmark import BenchmarkMeta
from evalscope.api.dataset import Sample
from evalscope.api.registry import register_benchmark
from evalscope.constants import Tags
from evalscope.utils.logger import get_logger

from ..aa_lcr.aa_lcr_adapter import AALCRAdapter, PROMPT_TEMPLATE

logger = get_logger()

_INDEX_KEY = '_aa_lcr_pruned_index'


@register_benchmark(
    BenchmarkMeta(
        name='aa_lcr_pruned',
        pretty_name='AA-LCR (Pruned)',
        tags=[Tags.KNOWLEDGE, Tags.REASONING, Tags.LONG_CONTEXT],
        description="""
## Overview

A pruned variant of AA-LCR that runs the same long-context QA scoring as the
upstream `aa_lcr` benchmark but only over a curated subset of indices
produced by the external universal pruner.

The selected-indices manifest is generated by the universal pruner in this
project (`pruner.cli build`) and is loaded at adapter init time. The
selection scoring combines metadata coverage need (here, `input_tokens`
bucketed into short / medium / long), three-model disagreement, score-
boundary uncertainty, and a small rarity bonus.

## Required extra parameter

- `selected_indices_path`: filesystem path to the manifest JSON.
""",
        dataset_id='evalscope/AA-LCR',
        metric_list=['acc'],
        few_shot_num=0,
        train_split=None,
        eval_split='test',
        prompt_template=PROMPT_TEMPLATE,
        extra_params={
            'selected_indices_path': {
                'type': 'str',
                'description': (
                    'Path to the pruner manifest JSON; the file must contain '
                    'a top-level "selected_indices" list of int indices.'
                ),
                'value': '',
            },
            'text_dir': {
                'type': 'str | null',
                'description': 'Local directory containing extracted AA-LCR text files; inherited from upstream.',
                'value': None,
            },
        },
    )
)
class AALCRPrunedAdapter(AALCRAdapter):
    """Subset of AA-LCR restricted to indices in a pruner manifest."""

    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)
        path = self.extra_params.get('selected_indices_path') or ''
        if not path:
            raise ValueError(
                "aa_lcr_pruned requires extra_params['selected_indices_path'] "
                'pointing at the pruner manifest JSON.'
            )
        manifest_path = Path(path)
        if not manifest_path.exists():
            raise FileNotFoundError(f'Pruner manifest not found: {manifest_path}')
        with manifest_path.open('r', encoding='utf-8') as fh:
            manifest = json.load(fh)
        self._selected_indices = set(manifest.get('selected_indices') or [])
        if not self._selected_indices:
            raise ValueError(f'Pruner manifest has no selected_indices: {manifest_path}')
        logger.info(
            f'aa_lcr_pruned: loaded {len(self._selected_indices)} '
            f'selected indices from {manifest_path}'
        )
        self._sample_counter = 0

    def record_to_sample(self, record: Dict[str, Any]) -> Sample:
        sample = super().record_to_sample(record)
        sample.metadata = dict(sample.metadata or {})
        sample.metadata[_INDEX_KEY] = self._sample_counter
        self._sample_counter += 1
        return sample

    def sample_filter(self, sample):
        idx = (sample.metadata or {}).get(_INDEX_KEY)
        if idx is None:
            return False
        return idx in self._selected_indices
