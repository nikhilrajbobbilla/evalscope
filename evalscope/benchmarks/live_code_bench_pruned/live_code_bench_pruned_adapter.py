"""LiveCodeBench Pruned adapter — runs the standard LiveCodeBench scoring
against a curated subset of indices produced by an external pruner.

Usage:
    evalscope eval --model <model> --datasets live_code_bench_pruned \\
        --extra-args '{"selected_indices_path": "/path/to/manifests/live_code_bench_pruned.json"}'

The manifest is the JSON object produced by `pruner.cli build` — it must
contain a `"selected_indices"` list of integers indexing into the upstream
LiveCodeBench dataset (ordering preserved by the parquet load).
"""
import json
from pathlib import Path
from typing import Any, Dict

from evalscope.api.benchmark import BenchmarkMeta
from evalscope.api.dataset import Sample
from evalscope.api.registry import register_benchmark
from evalscope.constants import Tags
from evalscope.utils.logger import get_logger

from ..live_code_bench.live_code_bench_adapter import LiveCodeBenchAdapter

logger = get_logger()

_INDEX_KEY = '_lcb_pruned_index'


@register_benchmark(
    BenchmarkMeta(
        name='live_code_bench_pruned',
        pretty_name='Live-Code-Bench (Pruned)',
        tags=[Tags.CODING],
        description="""
## Overview

A pruned variant of LiveCodeBench that runs the same scoring pipeline as the
upstream `live_code_bench` benchmark but only over a curated subset of
indices produced by an external pruner.

The selected-indices manifest is generated by the universal pruner in this
project (`pruner.cli build`) and is loaded at adapter init time. The
selection scoring combines metadata coverage need, model disagreement,
score-boundary uncertainty, and a small rarity bonus, so the pruned subset
preserves the structure of the original benchmark while letting reviewers
run a 10x cheaper evaluation that predicts full-benchmark accuracy within
a few percentage points (see docs/lomo_validation.md).

## Required extra parameter

- `selected_indices_path`: filesystem path to the manifest JSON.

Everything else (date filtering, sandbox config, format) is inherited from
the upstream `live_code_bench` adapter.
""",
        dataset_id='evalscope/livecodebench_code_generation_lite_parquet',
        subset_list=['release_v5'],
        metric_list=['acc'],
        aggregation='mean_and_pass_at_k',
        eval_split='test',
        prompt_template=(
            '### Question:\n{question_content}\n\n{format_prompt} ### Answer: '
            '(use the provided format with backticks)\n\n'
        ),
        review_timeout=6,
        extra_params={
            'selected_indices_path': {
                'type': 'str',
                'description': (
                    'Path to the pruner manifest JSON; the file must contain '
                    'a top-level "selected_indices" list of int indices.'
                ),
                'value': '',
            },
            'start_date': {
                'type': 'str | null',
                'description': 'Filter problems starting from this date (YYYY-MM-DD). Inherited from upstream.',
                'value': None,
            },
            'end_date': {
                'type': 'str | null',
                'description': 'Filter problems up to this date (YYYY-MM-DD). Inherited from upstream.',
                'value': None,
            },
            'debug': {
                'type': 'bool',
                'description': 'Enable verbose debug logging.',
                'value': False,
            },
        },
        sandbox_config={
            'image': 'python:3.11-slim',
            'tools_config': {
                'shell_executor': {},
                'python_executor': {},
            },
        },
    )
)
class LiveCodeBenchPrunedAdapter(LiveCodeBenchAdapter):
    """Subset of LiveCodeBench restricted to indices in a pruner manifest."""

    def __init__(self, **kwargs):
        super().__init__(**kwargs)
        path = self.extra_params.get('selected_indices_path') or ''
        if not path:
            raise ValueError(
                "live_code_bench_pruned requires extra_params['selected_indices_path'] "
                'pointing at the pruner manifest JSON.'
            )
        manifest_path = Path(path)
        if not manifest_path.exists():
            raise FileNotFoundError(f'Pruner manifest not found: {manifest_path}')
        with manifest_path.open('r', encoding='utf-8') as fh:
            manifest = json.load(fh)
        self._selected_indices = set(manifest.get('selected_indices') or [])
        if not self._selected_indices:
            raise ValueError(f'Pruner manifest has no selected_indices: {manifest_path}')
        logger.info(
            f'live_code_bench_pruned: loaded {len(self._selected_indices)} '
            f'selected indices from {manifest_path}'
        )
        # Counter used to tag each record with its position in the upstream dataset
        self._sample_counter = 0

    def record_to_sample(self, record: Dict[str, Any]) -> Sample:
        sample = super().record_to_sample(record)
        # Stamp the upstream-dataset index so sample_filter can use it.
        sample.metadata = dict(sample.metadata or {})
        sample.metadata[_INDEX_KEY] = self._sample_counter
        self._sample_counter += 1
        return sample

    def sample_filter(self, sample):
        idx = (sample.metadata or {}).get(_INDEX_KEY)
        if idx is None:
            return False
        if idx not in self._selected_indices:
            return False
        # Preserve the upstream date filter
        return super().sample_filter(sample)
